# List Type
test_list = [50, "100", True, 50, "50"]

print(test_list)
print(test_list[1])
print(len(test_list))
print(type(test_list))

# Tuple Type
test_tuple = (50, "100", True, 50, "50")
print(test_tuple)
print(len(test_tuple))
print(type(test_tuple))

# Set Type
test_set = {50, "100", True, 50, "50", 50, 100, 100, "a"}
print(test_set)
print(len(test_set))
print(type(test_set))

# Dict Type
test_dict = {"first": 50, "second": "100", "third": True, "a": 50, "a": "LAST"}
print(test_dict)
print(len(test_dict))
print(type(test_dict))
